public class Calculate {
	
	public String turnOn() {
		return "Turn On";
	}
	
	public String turnOff() {
		return "Turn Off";
	}
	
	public int add( int a, int b) {
		return a + b;
	}
	
	public int sub( int a, int b) {
		return a - b;
	}
	
	public int div(int num1,int num2){
		return num1 / num2;
	}
	
	public int mult(int num1, int num2){
		return num1 * num2;
	}

}
